// Regenerates src/game/easyJump.js from public/easy_jump.html.
//
// The game is authored/tested as a standalone HTML file; the app embeds it
// INLINE (no iframe) via this generated module. Re-run after editing the game:
//   node scripts/extract-easy-jump.mjs
//
// Transformations applied:
//  - CSS: the `html,body{...}` rule is rescoped to `#easyJumpHost` (the
//    overlay container), so the game styles never leak onto the app.
//  - JS: the IIFE wrapper becomes an exported bootEasyJump() with a kill
//    switch + cleanup (stops the rAF loop, removes the window listener), so
//    closing the overlay fully tears the game down.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const src = readFileSync(resolve(root, 'public/easy_jump.html'), 'utf8')

const fail = (what) => {
  console.error(`✗ extract-easy-jump: could not find ${what} in public/easy_jump.html`)
  process.exit(1)
}

// ── CSS ──
const cssMatch = src.match(/<style>([\s\S]*?)<\/style>/)
if (!cssMatch) fail('<style> block')
let css = cssMatch[1]
if (!css.includes('html,body{')) fail('the html,body{ rule (needed for rescoping)')
css = css.replace('html,body{', '#easyJumpHost{')

// ── Body markup (game shell) ──
const htmlMatch = src.match(/<body>([\s\S]*?)<script>/)
if (!htmlMatch) fail('<body>…<script> markup')
const html = htmlMatch[1].trim()

// ── Script (inside the IIFE) ──
const jsMatch = src.match(/<script>\s*\(function\(\)\{([\s\S]*?)\}\)\(\);?\s*<\/script>/)
if (!jsMatch) fail('the (function(){...})() game script')
let js = jsMatch[1]

// Kill-switch: stop the rAF loop when the overlay unmounts.
const loopTail = 'if (running) requestAnimationFrame(loop);'
if (!js.includes(loopTail)) fail(`the loop tail "${loopTail}"`)
js = js.replace(loopTail, 'if (running && !killed) requestAnimationFrame(loop);')
if (!js.includes("window.addEventListener('mouseup', onUp);")) fail('the window mouseup listener')

const out = `// GENERATED by scripts/extract-easy-jump.mjs from public/easy_jump.html.
// Do not edit by hand — edit the game file and re-run the script.
/* eslint-disable */
export const GAME_CSS = ${JSON.stringify(css)}

export const GAME_HTML = ${JSON.stringify(html)}

// Boots the game against the #easyJumpHost DOM (must already be mounted).
// Returns a cleanup function that stops the loop and removes global listeners.
export function bootEasyJump() {
  let killed = false
${js}
  return function cleanup() {
    killed = true
    running = false
    window.removeEventListener('mouseup', onUp)
  }
}
`

mkdirSync(resolve(root, 'src/game'), { recursive: true })
writeFileSync(resolve(root, 'src/game/easyJump.js'), out)
console.log(`✓ src/game/easyJump.js generated (${(out.length / 1024).toFixed(1)} KB)`)
